/*Atividade 4 – Contagem Regressiva
Nível: Intermediário
Crie um programa que:
Utilize um laço de repetição
Conte de 20 até 1
Exiba cada número no console
Quando o número for igual a 10, exiba a mensagem:
Metade do caminho!*/

for(let i = 20;i>=1;i--){
    if(i == 10){console.log(`${i} Metade do caminho!`)}
    else(console.log(i))
}

